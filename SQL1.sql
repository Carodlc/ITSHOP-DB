CREATE OR REPLACE PROCEDURE "OBTENER_DATOS_IMAGEN" (
    p_ruta_archivo IN VARCHAR2,
    p_datos_imagen OUT CLOB
)
AS
    v_archivo UTL_FILE.FILE_TYPE;
    v_buffer VARCHAR2(32767);
BEGIN
    -- Abrir el archivo en modo de solo lectura
    v_archivo := UTL_FILE.FOPEN('OBJETOS_LOB', p_ruta_archivo, 'R');

    -- Leer el contenido del archivo y almacenarlo en el CLOB
    DBMS_LOB.CREATETEMPORARY(p_datos_imagen, TRUE);
    LOOP
        UTL_FILE.GET_LINE(v_archivo, v_buffer);
        DBMS_LOB.WRITEAPPEND(p_datos_imagen, LENGTH(v_buffer), v_buffer);
    END LOOP;

EXCEPTION
    WHEN NO_DATA_FOUND THEN
        -- Si no se encuentra ning√∫n dato en el archivo, cerrar el archivo
        UTL_FILE.FCLOSE(v_archivo);
    WHEN UTL_FILE.INVALID_OPERATION THEN
        -- Si hay alg√∫n error al operar sobre el archivo, cerrar el archivo y propagar el error
        UTL_FILE.FCLOSE(v_archivo);
        RAISE;
END Obtener_Datos_Imagen;
